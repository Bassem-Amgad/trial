class calculator:
    def add (num1,num2):
        print(num1+num2)
    def sub (num1,num2):
        print(num1-num2)
    def multiply (num1,num2):
        print(num1*num2)
    def divide (num1,num2):
        print(num1/num2)

calculator.add(5,3)
calculator.sub(5,3)
calculator.multiply(5,3)
calculator.divide(5,3)
